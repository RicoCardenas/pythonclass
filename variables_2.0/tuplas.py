#creando tupla con tuple()
#de esta forma se puede crear tupla vacias
tupla = tuple(["dato1", "dato2", "dato3", "dato4", "dato5"])
print(tupla)

#2da forma de crear una tupla
tupla = "dato1","dato2"
print(tupla)

#tupla con un solo dato
tupla = "dato1",
print(tupla)

#la tupla se crea cuando son datos de solo lectura, no se pueden modificar